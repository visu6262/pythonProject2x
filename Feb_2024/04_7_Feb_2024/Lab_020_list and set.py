# list
# list is unordered collection of items
my_list = [1, 2, 3, 4, 5, 5]
print(my_list)  # [1, 2, 3, 4, 5, 5]
print("Data type of my_list :", type(my_list))

# set
# set is unordered collection of unique list
my_set_1 = {1, 2, 3, 4, 5, 5, 5}
print(my_set_1)  # {1, 2, 3, 4, 5}
print("Data type of my_list_1 :", type(my_set_1))
