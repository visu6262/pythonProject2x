# Assignment Operators
a = 5
# Assignment operators are used to assign values to variables
a +=4  # (same as) a =a + 4
print(a)
"""-----------"""
a = 5
a -=4  # (same as) a=a-4
print(a)
"""-----------"""
a=5
a *=4 # (same as) a=a*4
print(a)
"""-----------"""
a=5
a /=4 # (same as) a=a/4
print(a)
"""-----------"""
a=5
a %=4 # (same as) a=a%4
print(a)

