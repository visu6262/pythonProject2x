# loop

for a in range(5):
    print(a)
print("---------1")
for a in range(0, 10):
    print(a)
print("----------2")
for a in range(1, 11):
    print(a)
print("----------3 adding two number given range")
for a in range(1, 11, 2):
    print(a)
print("----------4 even numbers")
for a in range(2, 11, 2):
    print(a)
print("----------5 odd numbers")
for a in range(1, 11, 2):
    print(a)
print("----------6 desending order")
for a in range(10, 0, -1):
    print(a)
